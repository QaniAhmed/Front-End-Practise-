function copyMsg(textToCopy) {
  // Copy to clipboard
  navigator.clipboard
    .writeText(textToCopy)
    .then(() => {
      // Show message
      let copyMsg = document.querySelector(".copyContainer");
      copyMsg.classList.remove("hide");
      copyMsg.innerHTML = "Copied to clipboard!";

      // Hide message after 1 second
      setTimeout(() => {
        copyMsg.classList.add("hide");
      }, 1000);
    })
    .catch((err) => {
      console.error("Clipboard copy failed:", err);
    });
}
